def plot_voltage_traces(
    rec: RecorderSnapshot, report: DiagnosticsReport, output_dir: str, cfg: PlotConfig,
) -> None:
    """Sample neuron voltage traces for key populations."""
    if report.raw_voltages is None:
        return

    # Prioritise unhealthy populations; only include those with voltage data.
    ranked = _rank_populations_by_health(report)
    sampled: List[Tuple[str, str, int]] = []
    for rn, pn in ranked:
        if len(sampled) >= 6:
            break
        if (rn, pn) not in rec._pop_index:
            continue
        idx = rec._pop_index[(rn, pn)]
        if not np.all(np.isnan(report.raw_voltages[:, idx, :])):
            sampled.append((rn, pn, idx))
    if not sampled:
        return
    fig, axes = plt.subplots(len(sampled), 1, figsize=(cfg.timeline_width + 2, min(cfg.max_fig_height, len(sampled) * cfg.trace_row_height)), sharex=True)
    if len(sampled) == 1:
        axes = [axes]
    t_ms = np.arange(report.n_timesteps, dtype=np.float32) * rec.dt_ms
    for ax, (rn, pn, pidx) in zip(axes, sampled):
        v = report.raw_voltages[:, pidx, :]  # [T, sample_size]
        n_valid = int(rec._pop_sizes[pidx])
        n_show = min(8, n_valid, v.shape[1])
        for ni in range(n_show):
            col = _get_cmap("tab10")(ni)
            y = v[:, ni]
            valid_mask = ~np.isnan(y)
            if valid_mask.sum() > 1:
                ax.plot(t_ms[valid_mask], y[valid_mask], linewidth=0.5, alpha=0.7, color=col)
        ax.set_ylabel(f"{pn}\n({rn})", fontsize=6)
        ax.tick_params(labelsize=6)
    axes[-1].set_xlabel("Time (ms)")
    fig.suptitle("Sample Neuron Voltage Traces", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "07_voltage_traces.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 07_voltage_traces.png")


def plot_spike_raster(
    rec: RecorderSnapshot, report: DiagnosticsReport, output_dir: str, cfg: PlotConfig,
) -> None:
    """Spike raster plots for key regions."""
    if not rec._spike_times:
        return
    # Prioritise regions with the unhealthiest populations.
    ranked_regions = _rank_regions_by_health(report)
    existing = [rn for rn in ranked_regions if rn in rec._region_pop_indices][:6]
    if not existing:
        return
    fig, axes = plt.subplots(
        len(existing), 1,
        figsize=(cfg.timeline_width + 2, min(cfg.max_fig_height, len(existing) * cfg.compact_row_height)),
        sharex=True,
    )
    if len(existing) == 1:
        axes = [axes]
    t_ms_max = report.n_timesteps * rec.dt_ms
    for ax, rn in zip(axes, existing):
        y_offset = 0
        pop_names = [rec._pop_keys[i][1] for i in rec._region_pop_indices[rn]]
        pop_colors_raster = _get_cmap("tab10")
        for pi, pn in enumerate(pop_names):
            key = (rn, pn)
            if key not in rec._spike_times:
                continue
            n_neurons = len(rec._spike_times[key])
            n_show = min(80, n_neurons)
            col = pop_colors_raster(pi % 10)
            for ni in range(n_show):
                times = rec._spike_times[key][ni]
