def _plot_firing_rates(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """01 — Horizontal bar chart of mean firing rates per population."""
    fig, ax = plt.subplots(figsize=(cfg.timeline_width, min(cfg.max_fig_height, max(6, rec._n_pops * cfg.height_per_pop))))
    labels, values, colors = [], [], []
    for rn, rs in report.regions.items():
        for pn, ps in rs.populations.items():
            labels.append(f"{rn}:{pn}")
            values.append(ps.mean_fr_hz)
            colors.append(_pop_color(ps))
    y_pos = np.arange(len(labels))
    ax.barh(y_pos, values, color=colors, height=0.8)
    ax.set_yticks(y_pos)
    ax.set_yticklabels(labels, fontsize=6)
    ax.set_xlabel("Firing rate (Hz)")
    ax.set_title("Population Firing Rates  (green=bio-ok, orange=out-of-range, red=silent)")
    ax.axvline(0, color="k", linewidth=0.5)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "01_firing_rates.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 01_firing_rates.png")


def _plot_population_heatmap(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """02 — Population firing-rate heatmap over time."""
    if report.pop_rate_binned is None:
        return
    pop_keys = rec._pop_keys
    pr = report.pop_rate_binned  # [n_bins, n_pops]
    pr_hz = pr * (1000.0 / rec.config.rate_bin_ms)
    t_axis = np.arange(pr_hz.shape[0]) * rec.config.rate_bin_ms
    fig, ax = plt.subplots(figsize=(cfg.timeline_width + 2, min(cfg.max_fig_height, max(6, rec._n_pops * cfg.height_per_pop * 0.9))))
    vmax = float(np.percentile(pr_hz, 99)) or 1.0
    im = ax.imshow(
        pr_hz.T,
        aspect="auto",
        origin="lower",
        interpolation="none",
        extent=[t_axis[0], t_axis[-1], 0, rec._n_pops],
        cmap="hot",
        vmin=0,
        vmax=vmax,
    )
    ax.set_yticks(np.arange(rec._n_pops) + 0.5)
    ax.set_yticklabels([f"{rn}:{pn}" for rn, pn in pop_keys], fontsize=5)
    ax.set_xlabel("Time (ms)")
    ax.set_title("Population Firing Rate Heatmap (Hz)")
    plt.colorbar(im, ax=ax, label="Firing rate (Hz)")
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "02_population_heatmap.png"), dpi=120)
    plt.close(fig)
    print(f"  ✓ 02_population_heatmap.png")


def _plot_region_spectra(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """03 — Per-region normalised band-power bar charts."""
    osc = report.oscillations
    n_plots = len(report.region_keys or [])
    if n_plots == 0:
        return
    ncols = 4
    nrows = (n_plots + ncols - 1) // ncols
    fig, axes = plt.subplots(nrows, ncols, figsize=(ncols * cfg.col_width, nrows * cfg.compact_row_height))
    axes = np.array(axes).flatten()
    colors_bar = ["#3498db", "#2ecc71", "#f39c12", "#e74c3c", "#9b59b6"]
    for ax_i, rn in enumerate(report.region_keys or []):
        ax = axes[ax_i]
        bp = osc.region_band_power.get(rn, {})
        bands = list(bp.keys())
        powers = [bp[b] for b in bands]
        ax.bar(bands, powers, color=colors_bar[: len(bands)])
        ax.set_title(rn, fontsize=7, pad=2)
        ax.set_ylim(0, 1)
        ax.tick_params(labelsize=6)
        dom_freq = osc.region_dominant_freq.get(rn, 0.0)
        if dom_freq > 0:
            ax.text(0.98, 0.95, f"{dom_freq:.1f} Hz", ha="right", va="top",
                    transform=ax.transAxes, fontsize=7)
    for ax_i in range(n_plots, len(axes)):
        axes[ax_i].set_visible(False)
    fig.suptitle("Per-Region Power Spectra", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "03_region_spectra.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 03_region_spectra.png")


def _plot_coherence(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """04 / 04b / 04c — Cross-regional coherence matrices (theta, beta, gamma)."""
    osc = report.oscillations
    n_r = len(osc.region_order)
    for band_label, coh_mat, fname in [
        ("Theta (4–8 Hz)",   osc.coherence_theta, "04_coherence_theta.png"),
        ("Beta (13–30 Hz)",  osc.coherence_beta,  "04b_coherence_beta.png"),
        ("Gamma (30–100 Hz)", osc.coherence_gamma, "04c_coherence_gamma.png"),
    ]:
        fig, ax = plt.subplots(figsize=(max(8, n_r * 0.5), min(cfg.max_fig_height, max(6, n_r * 0.5))))
        cmap = plt.cm.YlOrRd.copy()  # type: ignore[attr-defined]
        cmap.set_bad(color="lightgrey")  # NaN = unsampled pair → grey
        im = ax.imshow(coh_mat, vmin=0, vmax=1, cmap=cmap, aspect="equal")
        ax.set_xticks(np.arange(n_r))
        ax.set_xticklabels(osc.region_order, rotation=90, fontsize=6)
        ax.set_yticks(np.arange(n_r))
        ax.set_yticklabels(osc.region_order, fontsize=6)
        ax.set_title(f"Cross-Regional Coherence — {band_label}")
        plt.colorbar(im, ax=ax, label="Coherence")
        plt.tight_layout()
        fig.savefig(os.path.join(output_dir, fname), dpi=150)
        plt.close(fig)
        print(f"  ✓ {fname}")


def _plot_isi_distributions(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """05 — ISI histograms for a selection of key populations (full mode only)."""
    if rec.config.mode != "full":
        return
    # Rank all populations by total spike count; show the top 12.
    def _spk(k: Tuple[str, str]) -> int:
        try:
            return report.regions[k[0]].populations[k[1]].total_spikes
        except KeyError:
            return 0

    key_pops = sorted(rec._spike_times.keys(), key=_spk, reverse=True)[:12]
    if not key_pops:
        return
    ncols = min(4, len(key_pops))
    nrows = (len(key_pops) + ncols - 1) // ncols
    fig, axes = plt.subplots(nrows, ncols, figsize=(ncols * cfg.col_width, nrows * cfg.row_height))
    axes = np.array(axes).flatten()
    for ax_i, key in enumerate(key_pops):
        ax = axes[ax_i]
        all_isis: List[float] = []
        for times in rec._spike_times[key]:
            if len(times) >= 2:
                all_isis.extend(np.diff(times))
        if all_isis:
            isis_ms = np.array(all_isis, dtype=np.float32) * rec.dt_ms
            ax.hist(
                isis_ms, bins=50, range=(0, float(np.percentile(isis_ms, 98))),
                color="#3498db", edgecolor="none", density=True
            )
            cv = np.std(isis_ms) / np.mean(isis_ms) if np.mean(isis_ms) > 0 else 0
            ax.set_title(f"{key[0]}:{key[1]}\nCV={cv:.2f}", fontsize=7)
            ax.set_xlabel("ISI (ms)", fontsize=7)
            ax.tick_params(labelsize=6)
    for ax_i in range(len(key_pops), len(axes)):
        axes[ax_i].set_visible(False)
    fig.suptitle("Inter-Spike Interval Distributions", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "05_isi_distributions.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 05_isi_distributions.png")


def _plot_homeostatic_gains(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """06 — Homeostatic gain trajectories over time."""
    hs = report.homeostasis
    if not hs.gain_trajectories or len(hs.gain_sample_times_ms) <= 1:
        return
    t_ms = hs.gain_sample_times_ms
    fig, ax = plt.subplots(figsize=(cfg.timeline_width, cfg.timeline_height))
    cmap = _get_cmap("tab20")
    for i, (key, traj) in enumerate(sorted(hs.gain_trajectories.items())):
        valid = ~np.isnan(traj)
        if valid.sum() < 2:
            continue
        ax.plot(t_ms[valid], traj[valid], linewidth=0.8, alpha=0.7,
                color=cmap(i % 20), label=key)
    ax.axhline(0.3, color="red", linestyle="--", linewidth=1.0, label="Collapse threshold")
    ax.axhline(1.0, color="gray", linestyle=":", linewidth=0.8)
    ax.set_xlabel("Time (ms)")
    ax.set_ylabel("g_L_scale")
    ax.set_title("Homeostatic Gain Trajectories")
    ax.set_ylim(0, 2.1)
    ax.legend(fontsize=5, ncol=3, loc="upper right")
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "06_homeostatic_gains.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 06_homeostatic_gains.png")


