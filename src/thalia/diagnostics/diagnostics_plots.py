def _pop_color(ps: PopulationStats) -> str:
    """Return a hex colour string summarising a population's health."""
    if ps.total_spikes == 0:
        return "#e74c3c"
    if ps.bio_plausibility == "ok":
        return "#2ecc71"
    if ps.bio_plausibility in ("low", "high"):
        return "#f39c12"
    return "#95a5a6"


def _unhealthiness(ps: PopulationStats) -> float:
    """Score how unhealthy a population is (higher = worse).

    Used to prioritize which populations appear in detail plots.
    Scoring:
    - OOB firing rate: distance from bio range, normalised by range midpoint
    - High pairwise correlation (synchrony pathology)
    - High burst fraction (epileptiform)
    - Silent populations get a fixed high score
    - In-range populations score ~0
    """
    score = 0.0

    # Firing rate distance from bio range
    if ps.bio_range_hz is not None:
        lo, hi = ps.bio_range_hz
        mid = (lo + hi) / 2.0 if (lo + hi) > 0 else 1.0
        if ps.mean_fr_hz < lo:
            score += (lo - ps.mean_fr_hz) / mid
        elif ps.mean_fr_hz > hi:
            score += (ps.mean_fr_hz - hi) / mid

    # Silent population
    if ps.total_spikes == 0:
        score += 5.0

    # Synchrony pathology
    if not np.isnan(ps.pairwise_correlation) and ps.pairwise_correlation > 0.15:
        score += ps.pairwise_correlation * 3.0

    # Epileptiform bursting
    if ps.fraction_burst_events > 0.03:
        score += ps.fraction_burst_events * 10.0

    # High SFA index (runaway adaptation)
    if not np.isnan(ps.sfa_index) and ps.sfa_index > 3.0:
        score += (ps.sfa_index - 3.0)

    return score


def _rank_populations_by_health(report: DiagnosticsReport) -> List[Tuple[str, str]]:
    """Return all (region_name, pop_name) pairs sorted by unhealthiness descending.

    Unhealthy populations come first; ties broken by total spikes (more active first).
    """
    items: List[Tuple[float, int, str, str]] = []
    for rs in report.regions.values():
        for ps in rs.populations.values():
            items.append((-_unhealthiness(ps), -ps.total_spikes, ps.region_name, ps.population_name))
    items.sort()
    return [(rn, pn) for _, _, rn, pn in items]


def _rank_regions_by_health(report: DiagnosticsReport) -> List[str]:
    """Return region names sorted by worst population unhealthiness descending."""
    region_scores: Dict[str, float] = {}
    for rs in report.regions.values():
        worst = max((_unhealthiness(ps) for ps in rs.populations.values()), default=0.0)
        region_scores[rs.region_name] = worst
    return sorted(region_scores, key=lambda rn: (-region_scores[rn], rn))


