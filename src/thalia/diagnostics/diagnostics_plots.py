def _plot_voltage_traces(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """07 — Sample neuron voltage traces for key populations (full mode only)."""
    if rec.config.mode != "full" or report.raw_voltages is None:
        return
    pop_keys = rec._pop_keys

    # Rank populations by total spike count; show the top 6 with voltage data.
    def _spk_v(rn_pn: Tuple[str, str]) -> int:
        try:
            return report.regions[rn_pn[0]].populations[rn_pn[1]].total_spikes
        except KeyError:
            return 0

    sampled: List[Tuple[str, str, int]] = []
    for rn, pn in sorted(pop_keys, key=_spk_v, reverse=True):
        if len(sampled) >= 6:
            break
        idx = rec._pop_index[(rn, pn)]
        if not np.all(np.isnan(report.raw_voltages[:, idx, :])):
            sampled.append((rn, pn, idx))
    if not sampled:
        return
    fig, axes = plt.subplots(len(sampled), 1, figsize=(cfg.timeline_width + 2, min(cfg.max_fig_height, len(sampled) * cfg.trace_row_height)), sharex=True)
    if len(sampled) == 1:
        axes = [axes]
    t_ms = np.arange(report.n_timesteps, dtype=np.float32) * rec.dt_ms
    for ax, (rn, pn, pidx) in zip(axes, sampled):
        v = report.raw_voltages[:, pidx, :]  # [T, sample_size]
        n_valid = int(rec._pop_sizes[pidx])
        n_show = min(8, n_valid, v.shape[1])
        for ni in range(n_show):
            col = _get_cmap("tab10")(ni)
            y = v[:, ni]
            valid_mask = ~np.isnan(y)
            if valid_mask.sum() > 1:
                ax.plot(t_ms[valid_mask], y[valid_mask], linewidth=0.5, alpha=0.7, color=col)
        ax.set_ylabel(f"{pn}\n({rn})", fontsize=6)
        ax.tick_params(labelsize=6)
    axes[-1].set_xlabel("Time (ms)")
    fig.suptitle("Sample Neuron Voltage Traces", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "07_voltage_traces.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 07_voltage_traces.png")


def _plot_raster(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """08 — Spike raster plots for key regions (full mode only)."""
    if rec.config.mode != "full" or not rec._spike_times:
        return
    # Rank regions by total spike count; show the top 6 most active.
    def _region_spikes(rn: str) -> int:
        rs = report.regions.get(rn)
        return sum(ps.total_spikes for ps in rs.populations.values()) if rs else 0

    existing = sorted(rec.brain.regions.keys(), key=_region_spikes, reverse=True)[:6]
    if not existing:
        return
    fig, axes = plt.subplots(
        len(existing), 1,
        figsize=(cfg.timeline_width + 2, min(cfg.max_fig_height, len(existing) * cfg.compact_row_height)),
        sharex=True,
    )
    if len(existing) == 1:
        axes = [axes]
    t_ms_max = report.n_timesteps * rec.dt_ms
    for ax, rn in zip(axes, existing):
        y_offset = 0
        region = rec.brain.regions[rn]
        pop_colors_raster = _get_cmap("tab10")
        for pi, pn in enumerate(region.neuron_populations.keys()):
            key = (rn, pn)
            if key not in rec._spike_times:
                continue
            n_neurons = len(rec._spike_times[key])
            n_show = min(80, n_neurons)
            col = pop_colors_raster(pi % 10)
            for ni in range(n_show):
                times = rec._spike_times[key][ni]
                if times:
                    t_vals = np.array(times, dtype=np.float32) * rec.dt_ms
                    ax.scatter(
                        t_vals, np.full_like(t_vals, y_offset + ni),
                        s=1.0, c=[col], marker="|", linewidths=0.5
                    )
            ax.axhline(y_offset + n_show, color="gray", linewidth=0.3)
            ax.text(
                t_ms_max * 1.002, y_offset + n_show / 2, pn,
                fontsize=6, va="center"
            )
            y_offset += n_show + 5
        ax.set_ylabel(rn, fontsize=8)
        ax.set_xlim(0, t_ms_max)
        ax.tick_params(labelsize=6)
    axes[-1].set_xlabel("Time (ms)")
    fig.suptitle("Spike Rasters (sample neurons)", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "08_raster_plots.png"), dpi=130)
    plt.close(fig)
    print(f"  ✓ 08_raster_plots.png")


def _plot_ei_phase_portrait(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """09 — E/I conductance phase portraits for key populations (full mode only)."""
    if (
        rec.config.mode != "full"
        or rec._g_exc_samples is None
        or rec._g_inh_samples is None
    ):
        return
    if np.all(np.isnan(rec._g_exc_samples)) or np.all(np.isnan(rec._g_inh_samples)):
        return
    pop_keys = rec._pop_keys
    key_ei_pops = ["relay", "ca1", "l23_pyr", "l4_pyr"]
    sampled_ei: List[Tuple[str, str, int]] = []
    for rn, pn in pop_keys:
        if any(t in pn for t in key_ei_pops) and len(sampled_ei) < 6:
            idx = rec._pop_index[(rn, pn)]
            sampled_ei.append((rn, pn, idx))
    if not sampled_ei:
        return
    fig, axes = plt.subplots(
        1, len(sampled_ei),
        figsize=(len(sampled_ei) * cfg.row_height, cfg.row_height),
    )
    if len(sampled_ei) == 1:
        axes = [axes]
    n_cond = rec._cond_sample_step
    for ax, (rn, pn, pidx) in zip(axes, sampled_ei):
        g_e = rec._g_exc_samples[:n_cond, pidx, :].flatten()
        g_i = rec._g_inh_samples[:n_cond, pidx, :].flatten()
        valid = ~(np.isnan(g_e) | np.isnan(g_i))
        if valid.sum() > 5:
            ax.scatter(g_e[valid], g_i[valid], s=2, alpha=0.4, c="#3498db")
        ax.set_xlabel("g_exc", fontsize=7)
        ax.set_ylabel("g_inh", fontsize=7)
        ax.set_title(f"{pn}\n({rn})", fontsize=7)
        ax.tick_params(labelsize=6)
    fig.suptitle("E/I Conductance Phase Portraits", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "09_ei_phase_portrait.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 09_ei_phase_portrait.png")


def _plot_spectrograms(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """10 — Time-frequency spectrograms of population rates per region."""
    n_steps = rec._n_recorded or rec.config.n_timesteps
    if n_steps <= 0 or rec._n_regions <= 0:
        return
    bin_steps = max(1, int(rec.config.rate_bin_ms / rec.dt_ms))
    n_bins = n_steps // bin_steps
    fs_bins = 1000.0 / rec.config.rate_bin_ms

    # Consume the pre-normalised region-rate matrix from the report (avoids
    # recomputing it with different normalisation from rec._region_spike_counts).
    if report.region_rate_binned is not None and report.region_rate_binned.shape == (n_bins, rec._n_regions):
        region_rate_binned = report.region_rate_binned
    else:
        # Fallback: recompute without population-size normalisation.
        region_rate_binned = np.zeros((n_bins, rec._n_regions), dtype=np.float32)
        for b in range(n_bins):
            start, end = b * bin_steps, (b + 1) * bin_steps
            region_rate_binned[b] = rec._region_spike_counts[start:end].sum(axis=0)

    n_col = min(4, rec._n_regions)
    n_row = (rec._n_regions + n_col - 1) // n_col
    fig_sg, axes_sg = plt.subplots(
        n_row, n_col,
        figsize=(n_col * cfg.col_width, n_row * cfg.row_height),
        squeeze=False,
        sharex=True,
    )
    nperseg_sg = min(n_bins, 128)
    for r_idx, rn in enumerate(rec._region_keys):
        ax = axes_sg[r_idx // n_col][r_idx % n_col]
        trace = region_rate_binned[:, r_idx].astype(float)
        if trace.std() > 0 and nperseg_sg >= 8:
            f_sg, t_sg, Sxx = sp_spectrogram(
                trace, fs=fs_bins, nperseg=nperseg_sg,
                noverlap=nperseg_sg // 2, window="hann", scaling="density"
            )
            f_mask = f_sg <= 100.0
            ax.pcolormesh(
                t_sg, f_sg[f_mask], 10 * np.log10(Sxx[f_mask] + 1e-12),
                shading="gouraud", cmap="magma",
            )
        ax.set_title(rn, fontsize=7)
        ax.set_ylabel("Freq (Hz)", fontsize=6)
        ax.tick_params(labelsize=5)
    for r_idx in range(rec._n_regions, n_row * n_col):
        axes_sg[r_idx // n_col][r_idx % n_col].set_visible(False)
