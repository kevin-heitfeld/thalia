def _plot_apical_basal_conductance(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """12 — Apical vs. basal AMPA conductance per population (full mode, TwoCompartmentLIF only)."""
    if (
        rec.config.mode != "full"
        or rec._g_apical_samples is None
        or rec._g_exc_samples is None
        or rec._cond_sample_step == 0
    ):
        return
    n_cond = rec._cond_sample_step

    labels: List[str] = []
    basal_means: List[float] = []
    apical_means: List[float] = []
    for pidx, (rn, pn) in enumerate(rec._pop_keys):
        apical_slab = rec._g_apical_samples[:n_cond, pidx, :]
        apical_mean = float(np.nanmean(apical_slab))
        if np.isnan(apical_mean):
            continue
        basal_slab = rec._g_exc_samples[:n_cond, pidx, :]
        basal_mean = float(np.nanmean(basal_slab))
        if np.isnan(basal_mean):
            basal_mean = 0.0
        labels.append(f"{pn}\n({rn})")
        basal_means.append(basal_mean)
        apical_means.append(apical_mean)

    if not labels:
        return

    n = len(labels)
    y = np.arange(n)
    bar_h = 0.35
    fig, ax = plt.subplots(figsize=(10, min(cfg.max_fig_height, max(4, n * 0.55))))
    ax.barh(y - bar_h / 2, basal_means, bar_h, label="Basal (g_exc AMPA)", color="#3498db", alpha=0.85)
    ax.barh(y + bar_h / 2, apical_means, bar_h, label="Apical (g_exc_apical)", color="#e67e22", alpha=0.85)
    ax.set_yticks(y)
    ax.set_yticklabels(labels, fontsize=7)
    ax.set_xlabel("Mean conductance")
    ax.set_title("Apical vs. Basal AMPA Conductance per Population")
    ax.legend(fontsize=8)
    ax.axvline(0, color="black", linewidth=0.5)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "12_apical_basal_conductance.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 12_apical_basal_conductance.png")


def _plot_voltage_distributions(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """13 — Per-population voltage distributions as violin plots (full mode only)."""
    if rec.config.mode != "full" or report.raw_voltages is None:
        return

    pops_with_data: List[Tuple[str, str, int]] = []
    for pidx, (rn, pn) in enumerate(rec._pop_keys):
        if not np.all(np.isnan(report.raw_voltages[:, pidx, :])):
            pops_with_data.append((rn, pn, pidx))

    if not pops_with_data:
        return

    n = len(pops_with_data)
    n_col = min(4, n)
    n_row = (n + n_col - 1) // n_col
    fig, axes = plt.subplots(n_row, n_col, figsize=(n_col * cfg.col_width, n_row * cfg.row_height), squeeze=False)

    for i, (rn, pn, pidx) in enumerate(pops_with_data):
        ax = axes[i // n_col][i % n_col]
        v_flat = report.raw_voltages[:, pidx, :].flatten()
        v_flat = v_flat[~np.isnan(v_flat)]
        if len(v_flat) < 10:
            ax.set_visible(False)
            continue
        vp = ax.violinplot(v_flat, showmedians=True, showextrema=False)
        for body in vp.get("bodies", []):
            body.set_facecolor("#3498db")
            body.set_alpha(0.7)
        rs = report.regions.get(rn)
        pop_stats = rs.populations.get(pn) if rs else None
        bc = (
            pop_stats.voltage_bimodality
            if pop_stats is not None and not np.isnan(pop_stats.voltage_bimodality)
            else None
        )
        subtitle = f"({rn})"
        if bc is not None:
            subtitle += f"  BC={bc:.3f}"
        ax.set_title(f"{pn}\n{subtitle}", fontsize=6)
        ax.set_xticks([])
        ax.set_ylabel("Voltage (mV)", fontsize=6)
        ax.tick_params(labelsize=6)

    for i in range(len(pops_with_data), n_row * n_col):
        axes[i // n_col][i % n_col].set_visible(False)

    fig.suptitle("Population Voltage Distributions", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "13_voltage_distributions.png"), dpi=130)
    plt.close(fig)
    print(f"  ✓ 13_voltage_distributions.png")


def _plot_neuromodulator_conc_by_region(
    rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: PlotConfig
) -> None:
    """14 — Neuromodulator receptor concentrations grouped by brain region."""
    if not report.neuromodulator_levels:
        return
    t_ms = report.homeostasis.gain_sample_times_ms
    nm_levels = report.neuromodulator_levels  # keys: "{region}/{mod_name}"

    by_region: Dict[str, List[Tuple[str, np.ndarray]]] = {}
    for key in sorted(nm_levels.keys()):
        rn, mod_name = key.split("/", 1)
        if rn not in by_region:
            by_region[rn] = []
        by_region[rn].append((mod_name, nm_levels[key]))

    if not by_region:
        return

    n_regions = len(by_region)
    n_col = min(3, n_regions)
    n_row = (n_regions + n_col - 1) // n_col
    fig, axes = plt.subplots(
        n_row, n_col,
        figsize=(n_col * (cfg.col_width + 1.0), n_row * cfg.row_height),
        squeeze=False,
        sharex=True,
    )
    colors = [_get_cmap("tab10")(i) for i in range(10)]

    for r_i, (rn, mod_list) in enumerate(sorted(by_region.items())):
        ax = axes[r_i // n_col][r_i % n_col]
        for m_i, (mod_name, traj) in enumerate(mod_list):
            valid = ~np.isnan(traj)
            if valid.sum() > 1 and len(t_ms) >= valid.sum():
                ax.plot(
                    t_ms[: valid.sum()], traj[valid],
                    linewidth=1.2, color=colors[m_i % 10], label=mod_name,
                )
        ax.set_title(rn, fontsize=8)
        ax.set_ylabel("Concentration", fontsize=6)
        ax.set_ylim(-0.02, 1.05)
        ax.axhline(0, color="gray", linewidth=0.5, linestyle=":")
        ax.legend(fontsize=5, loc="upper right")
        ax.tick_params(labelsize=5)

    for r_i in range(n_regions, n_row * n_col):
        axes[r_i // n_col][r_i % n_col].set_visible(False)

    fig.supxlabel("Time (ms)", fontsize=8)
    fig.suptitle("Neuromodulator Concentrations by Region", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "14_neuromodulator_conc_by_region.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 14_neuromodulator_conc_by_region.png")
