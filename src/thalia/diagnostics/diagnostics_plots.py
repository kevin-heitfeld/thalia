"""
Diagnostics plots — all matplotlib visualisations for a DiagnosticsReport.

The public entry point is :func:`plot`, which dispatches to numbered
``_plot_*`` helper functions.  A failure in one plot never prevents the
remaining plots from being saved.
"""

from __future__ import annotations

import os
from dataclasses import dataclass
from typing import TYPE_CHECKING, Callable, Dict, List, Optional, Tuple

import matplotlib
import numpy as np
from scipy.signal import spectrogram as sp_spectrogram

from .diagnostics_types import DiagnosticsReport, PopulationStats

if TYPE_CHECKING:
    from .diagnostics_recorder import DiagnosticsRecorder

matplotlib.use("Agg")
import matplotlib.pyplot as plt


@dataclass
class PlotConfig:
    """Scale factors for diagnostic figure sizes.

    Pass a customised instance to :func:`plot` to adapt layout for very
    large brains (>100 populations) or custom display environments.
    """

    col_width: float = 4.0           # inches per subplot column
    row_height: float = 3.0          # inches per subplot row (normal panels)
    compact_row_height: float = 2.5  # inches per subplot row (compact panels)
    trace_row_height: float = 1.8    # inches per time-trace row
    height_per_pop: float = 0.2      # inches per population in vertical listings
    timeline_width: float = 14.0     # width (inches) for full-width time-series plots
    timeline_height: float = 5.0     # height (inches) for fixed-height time-series plots
    max_fig_height: float = 40.0     # hard cap on figure height (inches)


def _get_cmap(name: str, n: int | None = None):
    """Return a Matplotlib colormap using the non-deprecated colormaps registry."""
    cmap = matplotlib.colormaps[name]
    return cmap if n is None else cmap.resampled(n)


# =============================================================================
# PUBLIC ENTRY POINT
# =============================================================================


def plot(rec: "DiagnosticsRecorder", report: DiagnosticsReport, output_dir: str, cfg: Optional[PlotConfig] = None) -> None:
    """Generate and save diagnostic plots to ``output_dir``.

    Requires ``matplotlib``.  Silently skips if it is not installed.
    Each numbered plot is generated by a dedicated ``_plot_*`` function so that
    a failure in one plot never prevents the remaining plots from being saved.
    """
    cfg = cfg or PlotConfig()
    os.makedirs(output_dir, exist_ok=True)
    plot_fns: list[Callable[["DiagnosticsRecorder", DiagnosticsReport, str, PlotConfig], None]] = [
        _plot_firing_rates,
        _plot_population_heatmap,
        _plot_region_spectra,
        _plot_coherence,
        _plot_isi_distributions,
        _plot_homeostatic_gains,
        _plot_stp_efficacy,
        _plot_voltage_traces,
        _plot_raster,
        _plot_ei_phase_portrait,
        _plot_spectrograms,
        _plot_neuromodulator_conc,
        _plot_apical_basal_conductance,
        _plot_voltage_distributions,
        _plot_neuromodulator_conc_by_region,
    ]
    failed_plots: list[str] = []
    for fn in plot_fns:
        try:
            fn(rec, report, output_dir, cfg)
        except Exception as exc:  # noqa: BLE001
            failed_plots.append(f"{fn.__name__}: {exc}")
    print(f"\n  All plots saved to: {output_dir}")
    if failed_plots:
        print(f"  WARNING: {len(failed_plots)} plot(s) failed:")
        for msg in failed_plots:
            print(f"    - {msg}")


