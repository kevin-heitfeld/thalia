def plot_isi_distributions(
    rec: RecorderSnapshot, report: DiagnosticsReport, output_dir: str, cfg: PlotConfig,
) -> None:
    """ISI histograms for a selection of key populations."""
    # Prioritise unhealthy populations; only include those with spike time data.
    ranked = _rank_populations_by_health(report)
    key_pops = [(rn, pn) for rn, pn in ranked if (rn, pn) in rec._spike_times][:12]
    if not key_pops:
        return
    ncols = min(4, len(key_pops))
    nrows = (len(key_pops) + ncols - 1) // ncols
    fig, axes = plt.subplots(nrows, ncols, figsize=(ncols * cfg.col_width, nrows * cfg.row_height))
    axes = np.array(axes).flatten()
    for ax_i, key in enumerate(key_pops):
        ax = axes[ax_i]
        all_isis: List[float] = []
        for times in rec._spike_times[key]:
            if len(times) >= 2:
                all_isis.extend(np.diff(times))
        if all_isis:
            isis_ms = np.array(all_isis, dtype=np.float32) * rec.dt_ms
            ax.hist(
                isis_ms, bins=50, range=(0, float(np.percentile(isis_ms, 98))),
                color="#3498db", edgecolor="none", density=True
            )
            cv = np.std(isis_ms) / np.mean(isis_ms) if np.mean(isis_ms) > 0 else 0
            ax.set_title(f"{key[0]}:{key[1]}\nCV={cv:.2f}", fontsize=7)
            ax.set_xlabel("ISI (ms)", fontsize=7)
            ax.tick_params(labelsize=6)
    for ax_i in range(len(key_pops), len(axes)):
        axes[ax_i].set_visible(False)
    fig.suptitle("Inter-Spike Interval Distributions", fontsize=10)
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "05_isi_distributions.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 05_isi_distributions.png")


def plot_homeostatic_gains(
    rec: RecorderSnapshot, report: DiagnosticsReport, output_dir: str, cfg: PlotConfig,
) -> None:
    """Homeostatic gain trajectories over time."""
    hs = report.homeostasis
    if not hs.gain_trajectories or len(hs.gain_sample_times_ms) <= 1:
        return
    t_ms = hs.gain_sample_times_ms
    fig, ax = plt.subplots(figsize=(cfg.timeline_width, cfg.timeline_height))
    cmap = _get_cmap("tab20")
    for i, (key, traj) in enumerate(sorted(hs.gain_trajectories.items())):
        valid = ~np.isnan(traj)
        if valid.sum() < 2:
            continue
        ax.plot(t_ms[valid], traj[valid], linewidth=0.8, alpha=0.7,
                color=cmap(i % 20), label=key)
    ax.axhline(0.3, color="red", linestyle="--", linewidth=1.0, label="Collapse threshold")
    ax.axhline(1.0, color="gray", linestyle=":", linewidth=0.8)
    ax.set_xlabel("Time (ms)")
    ax.set_ylabel("g_L_scale")
    ax.set_title("Homeostatic Gain Trajectories")
    ax.set_ylim(0, 2.1)
    ax.legend(fontsize=5, ncol=3, loc="upper right")
    plt.tight_layout()
    fig.savefig(os.path.join(output_dir, "06_homeostatic_gains.png"), dpi=150)
    plt.close(fig)
    print(f"  ✓ 06_homeostatic_gains.png")


