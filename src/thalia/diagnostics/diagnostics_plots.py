def _pop_color(ps: PopulationStats) -> str:
    """Return a hex colour string summarising a population's health."""
    if ps.total_spikes == 0:
        return "#e74c3c"
    if ps.bio_plausibility == "ok":
        return "#2ecc71"
    if ps.bio_plausibility in ("low", "high"):
        return "#f39c12"
    return "#95a5a6"


